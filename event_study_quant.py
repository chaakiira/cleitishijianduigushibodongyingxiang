# 2. 工具函数
# ============================================================

def clean_alias(name: str) -> str:
    """去掉括号内的涨跌备注，方便匹配"""
    name = ALIAS_PATCH.get(name, name)
    name = re.sub(r"（[^）]*）", "", name).strip()
    return name


def extract_companies(text: str) -> list[str]:
    """
    从「影响公司/行业」文本中提取具体公司名。
    策略:
      - 分号前半段往往是具体公司（如：A、B、C；D产业链）
      - 去掉纯板块描述词
    """
    if pd.isna(text):
        return []
    # 用分号分割，取第一段（公司名段）和第二段（板块段）
    parts = re.split(r"[；;]", text)
    companies = []
    for part in parts:
        # 按顿号/逗号/中文逗号分割
        tokens = re.split(r"[、，,]", part)
        for tok in tokens:
            tok = tok.strip()
            if not tok:
                continue
            # 跳过纯行业/板块描述
            skip_keywords = ["板块", "产业链", "ETF", "指数", "权重股",
                             "龙头", "概念", "行业", "企业", "标的",
                             "等", "等小金属"]
            if any(kw in tok for kw in skip_keywords):
                continue
            # 跳过过短（<=1字）或过长（>=12字，一般是描述）
            if len(tok) <= 1 or len(tok) >= 12:
                continue
            companies.append(clean_alias(tok))
    # 去重保序
    seen = set()
    result = []
    for c in companies:
        if c not in seen:
            seen.add(c)
            result.append(c)
    return result


def match_codes(companies: list[str]) -> list[tuple[str, str, str]]:
    """
    返回 list of (公司名, 股票代码, 备注)
    未匹配到的返回代码='未匹配'
    """
    results = []
    for c in companies:
        if c in STOCK_MAP:
            code, note = STOCK_MAP[c]
            results.append((c, code, note))
        else:
            # 尝试模糊匹配（公司名包含映射键）
            found = False
            for key, (code, note) in STOCK_MAP.items():
                if key in c or c in key:
                    results.append((c, code, f"模糊匹配←{key}"))
                    found = True
                    break
            if not found:
                results.append((c, "未匹配", ""))
    return results

# ============================================================
# 3. 量化特征生成
# ============================================================

INTENSITY_MAP   = {"高": 3, "中": 2, "低": 1}
DIRECTION_MAP   = {"正面": 1, "负面": -1, "双向": 0}
TYPE_CATEGORIES = ["宏观/地缘", "行业/技术", "政策"]


def build_features(df: pd.DataFrame) -> pd.DataFrame:
    df = df.copy()

    # 列名标准化（去掉换行符）
    df.columns = [c.replace("\n", "") for c in df.columns]

    # 日期解析
    df["发生日期"] = pd.to_datetime(df["发生日期"], errors="coerce")
    df["年份"]     = df["发生日期"].dt.year
    df["月份"]     = df["发生日期"].dt.month
    df["季度"]     = df["发生日期"].dt.quarter
    df["星期几"]   = df["发生日期"].dt.day_name()
    df["是否周末前"] = df["发生日期"].dt.dayofweek.isin([3, 4]).astype(int)  # 周四/五

    # 窗口特征
    win_start_col = [c for c in df.columns if "窗口开始" in c][0]
    win_end_col   = [c for c in df.columns if "窗口结束" in c][0]
    df["窗口开始_天数"] = pd.to_numeric(df[win_start_col], errors="coerce")
    df["窗口结束_天数"] = pd.to_numeric(df[win_end_col],   errors="coerce")
    df["窗口长度"]      = df["窗口结束_天数"] - df["窗口开始_天数"] + 1
    df["前置观察期"]    = (-df["窗口开始_天数"]).clip(lower=0)   # 事件前观察天数

    # 强度 & 方向数值化
    df["影响强度_数值"] = df["影响强度"].map(INTENSITY_MAP).fillna(0).astype(int)
    df["影响方向_数值"] = df["影响方向"].map(DIRECTION_MAP).fillna(0).astype(int)
    df["综合得分"]      = df["影响强度_数值"] * df["影响方向_数值"]

    # 方向独热
    df["方向_正面"] = (df["影响方向"] == "正面").astype(int)
    df["方向_负面"] = (df["影响方向"] == "负面").astype(int)
    df["方向_双向"] = (df["影响方向"] == "双向").astype(int)

    # 类型独热
    for cat in TYPE_CATEGORIES:
        df[f"类型_{cat}"] = df["类型"].str.contains(cat, na=False).astype(int)

    # 公司提取 & 代码匹配
    df["提取公司列表"] = df["影响公司/行业"].apply(extract_companies)
    df["公司数量"]     = df["提取公司列表"].apply(len)

    match_results          = df["提取公司列表"].apply(match_codes)
    df["匹配明细"]         = match_results.apply(
        lambda rows: " | ".join(f"{c}({code})" for c, code, _ in rows if code != "SECTOR")
    )
    df["已匹配代码列表"]   = match_results.apply(
        lambda rows: [code for _, code, _ in rows if code not in ("未匹配", "SECTOR")]
    )
    df["匹配代码数量"]     = df["已匹配代码列表"].apply(len)
    df["未匹配公司"]       = match_results.apply(
        lambda rows: " | ".join(c for c, code, _ in rows if code == "未匹配")
    )
    df["是否有明确个股"]   = (df["匹配代码数量"] > 0).astype(int)

    # 代码列表转字符串（便于 Excel 显示）
    df["已匹配代码_合并"]  = df["已匹配代码列表"].apply(lambda x: " | ".join(x))
    df["提取公司_合并"]    = df["提取公司列表"].apply(lambda x: " | ".join(x))

    # 删除中间列表对象列（Excel 无法存储）
    df.drop(columns=["提取公司列表", "已匹配代码列表", "匹配明细"], inplace=True)

    return df

# ============================================================
# 4. 每家公司独立展开表（长表）
# ============================================================

def build_long_table(df_feat: pd.DataFrame, df_raw: pd.DataFrame) -> pd.DataFrame:
    """
    将每条事件按提取到的公司展开为行，方便后续 AR/CAR 计算。
    """
    raw_cols = ["事件名称", "类型", "发生日期", "影响强度", "影响方向",
                "影响强度_数值", "影响方向_数值", "综合得分",
                "窗口开始_天数", "窗口结束_天数", "窗口长度"]
    rows = []
    for _, r in df_feat.iterrows():
        companies = r["提取公司_合并"].split(" | ") if r["提取公司_合并"] else []
        codes     = r["已匹配代码_合并"].split(" | ") if r["已匹配代码_合并"] else []
        # 如果没提取到公司，也保留一行（板块级事件）
        if not companies:
            row = {c: r[c] for c in raw_cols if c in r.index}
            row["公司名称"] = ""
            row["股票代码"] = ""
            row["交易所"]   = ""
            row["代码状态"] = "板块/行业事件"
            rows.append(row)
        else:
            for i, comp in enumerate(companies):
                code = codes[i] if i < len(codes) else "未匹配"
                row = {c: r[c] for c in raw_cols if c in r.index}
                row["公司名称"] = comp
                row["股票代码"] = code
                row["交易所"]   = ("上交所" if code.endswith(".SH") else
                                   "深交所" if code.endswith(".SZ") else
                                   "未匹配" if code == "未匹配" else code)
                row["代码状态"] = ("已匹配" if code not in ("未匹配", "") else "未匹配")
                rows.append(row)
    long_df = pd.DataFrame(rows)
    # 估计期设置（用于 AR 计算的参考）
    long_df["估计期开始_天数"] = -120
    long_df["估计期结束_天数"] = -2
    long_df["估计期长度"]      = 119
    return long_df

